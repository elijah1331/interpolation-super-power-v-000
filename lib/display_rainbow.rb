def display_rainbow(array_of_colors)
  puts "R: #{array_of_colors[0]}, O: #{array_of_colors[1]},"
  puts "Y: #{array_of_colors[2]}, G: #{array_of_colors[3]},"
  puts "B: #{array_of_colors[4]}, I: #{array_of_colors[5]},"
  puts "V: #{array_of_colors[6]}"
end
